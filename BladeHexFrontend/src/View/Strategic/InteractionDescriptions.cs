    private static readonly Random Rng = new();
    public static string GetTownDescription(string townName, string townType, int prosperity, int garrison)
    {
        if (townType == "village") return prosperity >= 50 ? Pick(GetPoolOrFallback("village_rich", VR), townName, prosperity, garrison) : Pick(GetPoolOrFallback("village_poor", VP), townName, prosperity, garrison);
        if (prosperity >= 70) return Pick(GetPoolOrFallback("town_rich", TR), townName, prosperity, garrison);
        if (prosperity >= 40) return Pick(GetPoolOrFallback("town_mid", TM), townName, prosperity, garrison);
        return Pick(GetPoolOrFallback("town_poor", TP), townName, prosperity, garrison);
    }
    public static string GetEnemyDescription(string enemyName, bool isHostile, int partySize, string entityType)
    {
        if (!isHostile) return Pick(GetNPool(entityType), enemyName, partySize, entityType);
        return Pick(GetHPool(entityType), enemyName, partySize, entityType);
    }
    public static string GetCastleDescription(string name, int garrison) => Pick(GetPoolOrFallback("castle", CD), name, garrison, 0);

    private static string[] GetHPool(string t) => t switch
    {
        "BanditParty" or "RobberParty" => GetPoolOrFallback("hostile_bandit", BH),
        "RaidingParty" => GetPoolOrFallback("hostile_raider", RH),
        "LordArmy" => GetPoolOrFallback("hostile_lord", LH),
        "EpicMonster" => GetPoolOrFallback("hostile_monster", MH),
        "PirateCrew" => GetPoolOrFallback("hostile_pirate", PH),
        _ => GetPoolOrFallback("hostile_generic", GH),
    };

    private static string[] GetNPool(string t) => t switch
    {
        "Adventurer" => GetPoolOrFallback("neutral_adventurer", AN),
        "Caravan" => GetPoolOrFallback("neutral_caravan", CN),
        "LordArmy" => GetPoolOrFallback("neutral_lord", LN),
        _ => GetPoolOrFallback("neutral_generic", GN),
    };

    /// <summary>从DescriptionLoader获取池，失败时使用硬编码回退</summary>
    private static string[] GetPoolOrFallback(string key, string[] fallback)
    {
        var pool = DescriptionLoader.GetPool(key);
        return pool ?? fallback;
    }

    private static string Pick(string[] pool, string n, int v1, int v2) => string.Format(pool[Rng.Next(pool.Length)], n, v1, v2);
    private static string Pick(string[] pool, string n, int v1, string v2) => string.Format(pool[Rng.Next(pool.Length)], n, v1, v2);

